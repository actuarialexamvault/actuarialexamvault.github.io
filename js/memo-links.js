// Function to get examiners report link for a given paper
function getExaminersReportLink(subject, session, year, paper = null) {
  try {
    let sessionUpper = session.toUpperCase();
    
    // Handle OCTOBER as alias for NOVEMBER
    if (sessionUpper === 'OCTOBER') {
      sessionUpper = 'NOVEMBER';
    }
    
    // Navigate through the nested structure
    if (examinersReportLinksMap[subject] && 
        examinersReportLinksMap[subject][year] && 
        examinersReportLinksMap[subject][year][sessionUpper]) {
      
      const sessionReports = examinersReportLinksMap[subject][year][sessionUpper];
      
      // If paper is specified, try to get paper-specific report first
      if (paper && sessionReports[paper]) {
        return sessionReports[paper];
      }
      
      // Otherwise return general report
      if (sessionReports.general) {
        return sessionReports.general;
      }
      
      // If no general report, return first available report
      const reportKeys = Object.keys(sessionReports);
      if (reportKeys.length > 0) {
        return sessionReports[reportKeys[0]];
      }
    }
    
    return null;
  } catch (error) {
    console.error('Error getting examiners report link:', error);
    return null;
  }
}

// Function to check if examiners report exists for given paper
function hasExaminersReport(subject, session, year, paper = null) {
  const link = getExaminersReportLink(subject, session, year, paper);
  return link !== null && link !== undefined;
}

